#Area of a circle
radius = input("Enter the radius of a circle")
pi=3.142
radius=int(radius)
print("The Area of the circle " +str(pi*radius*radius))

#volume of a cube
length = input("Enter the length of the cube")
length=int(length)
print(" The volume of the cube is " +str(length*length*length))

#volume of a cylinder
radius = input("Enter the radius of a circle")
pi=3.142
radius=int(radius)
height_of_cylinder= 15
volume = (pi * radius * radius * height_of_cylinder)
print(" The Volume of the cylinder is "+str(volume))

#surface area of an hollow
pi=3.142
height_of_cylinder = 15
diameter=input("Enter the diameter of a circle")
diameter=int(diameter)
surfacearea = (pi * diameter * height_of_cylinder) 
print("The Surface Area of the hollow is " +str(surfacearea))

